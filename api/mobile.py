def build_features(m: MobileSchema):
    numeric = [
        m.Rating,
        m.Num_Ratings,
        m.RAM,
        m.ROM,
        m.Back_Cam,
        m.Front_Cam,
        m.Battery
    ]

    processor = [1 if m.Processor == i else 0 for i in processor_list]

    date = [1 if m.Scrap_Date == '2023-06-17' else 0]

    return numeric + processor + date

@mobile_router.post('/')
async def predict(mobile: MobileSchema):
    features = build_features(mobile)
    scaled_data = scaler.transform([features])
    pred = linear_model.predict(scaled_data)[0]

    return {'predicted_price': round(pred, 2)}